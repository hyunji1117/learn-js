/*
산술 연산자 +, -, *, /, %, **
*/

console.log(1 + 4 * 5); // 21

console.log((1 + 4) * 5); // 25

console.log(9 / 4); // 2.25

console.log(9 % 4); // 1

var age = 2 ** 3; // 8
console.log('나이는 ' + age + '살 입니다.'); // 나이는 8세입니다. 
console.log(`나이는 ${age}짤 입니다.`); // 나이는 여덟살 입니다. 예약하는 텍스트를 먼저 작성한다. 그리고 변수로 바꿀 부분을 작성한다. 




